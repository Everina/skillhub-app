interface PixelSealProps {
  scale?: number;
  className?: string;
}

export default function PixelSeal({ scale = 10, className = "" }: PixelSealProps) {
  const rows = GRID.length;
  const cols = GRID[0].length;
  const W = cols * scale;
  const H = rows * scale;

  return (
    <svg
      width={W}
      height={H}
      viewBox={`0 0 ${W} ${H}`}
      className={className}
      style={{ imageRendering: "pixelated" }}
      aria-label="Pixel art seal"
    >
      {GRID.map((row, ri) =>
        row.map((cell, ci) => {
          if (cell === 0) return null;
          return (
            <rect
              key={`${ri}-${ci}`}
              x={ci * scale}
              y={ri * scale}
              width={scale}
              height={scale}
              fill={C[cell]}
            />
          );
        })
      )}
    </svg>
  );
}
